#include <unistd.h>

void	putc(char c)
{
	write (1, &c, 1);
}

void	maff()
{
	char a = 'a';
	char B = 'B';

	while (a <= 'y')
	{
		putc(a);
		putc(B);
		a += 2;
		B += 2;
	}
	putc('\n');
}

int main()
{
	maff();
	return (0);
}
