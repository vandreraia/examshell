char	*ft_strrev(char *str)
{
	int i;
	int j;
	char *rev; //incializa essa bosta

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	while (i >= 0)
	{
		rev[j] = str[i];
		i--;
		j++;
	}
	return (rev);
}
