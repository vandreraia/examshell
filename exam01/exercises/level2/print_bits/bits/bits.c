#include <unistd.h>

void	bits(unsigned char bit)
{
	int bite = bit;
	int n128 = 128;
	
	while (n128 > 0)
	{
		if (n128 <= bit)
		{
			write(1, "1", 1);
			bit %= 128;
		}
		else
			write(1, "0", 1);
		n128 /= 2;
	}
}
int main()
{
	bits(128);
	write(1, "\n", 1);
	bits(68);
	write(1, "\n", 1);
	bits(255);
	write(1, "\n", 1);
	bits(6);
	write(1, "\n", 1);
	bits(1);
	return (0);
}
